#include <stdio.h>

int main() {
    printf("Hello World!");

    // int - egész szám
    // double - tört számok
    // char - karakter

    // unsigned - előjel nélküli

    int num = 105;

    printf("\n%d", num);

    char karakter = 'A';

    printf("\n%c", karakter);
    printf("\n%c", num);

    printf("\n%.2f", 5.0 / 2.0);
}
